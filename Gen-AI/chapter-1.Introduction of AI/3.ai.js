
                  //         Branches of ai

// 1. machine learning- it is a subset or branch  of ai that focuses on developing algorithms and models that allow computers to learn from data and make predictions or decisions without being explicitly programmed.

// 2.deep learning- it is a subset of machine learning that focuses on using artificial neural networks with multiple layers to model and understand complex patterns in data. deep learning has been particularly successful in tasks such as image recognition, natural language processing, and speech recognition.

// 3. natural language processing- it is a branch of ai that focuses on enabling computers to understand, interpret, and generate human language in a valuable way.

// 4.generative ai- it is a branch of ai that focuses on creating models and algorithms capable of generating new content, such as text, images, videos, music, and more. based on prompts or input data. gen ai models learn patterns and structures from large datasets and use that knowledge to produce optput.

// 5. computer vision- it is a field of ai that enables computers to interpret and understand the visual world, similar to how humans do.

// 6. robotics- it is a branch of ai that focuses on designing and building intelligent machines capable of performing tasks autonomously or with minimal human intervention.

// 7. expert systems- it is a branch of ai that focuses on creating computer programs that mimic the decision-making abilities of a human expert in a specific domain.

