// ++(increment) and --(decrement) operator are unary operator

// increment and decrement operator work in two ways 



//1)post increment and pre  increment
let a=9;
// post increment 
console.log(a++)
console.log(a)
//pre increment
console.log(++a)



//2)post decrement and pre decrement
 let b=10
//post decrement
console.log(b--)
//pre decremnt
console.log(--b)


